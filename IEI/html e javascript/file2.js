function soma(x, y) {
    return x + y;
}

function subtracao(x, y) {
    return x - y;
}

function multiplicacao(x, y) {
    return x * y;
}

function divisao(x, y) {
    return x / y;
}

var resultado1 = soma(3, 4);
var resultado2 = subtracao(3, 4);
var resultado3 = multiplicacao(3, 4);
var resultado4 = divisao(3, 4);
console.log(resultado1);
console.log(resultado2);
console.log(resultado3);
console.log(resultado4);